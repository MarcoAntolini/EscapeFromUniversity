package escapefromuniversity.model;

public enum GameCollisionType {
	
	/**
	 * identifies a obstacle object.
	 */
	OBSTACLE,
	/**
	 * identifies a boss or protagonist.
	 */
	ENTITY,
	/**
	 * identifies a immune boss or protagonist.
	 */
	IMMUNE_ENTITY,
	/**
	 * identifies a bullet
	 */
	BULLET,
	/**
	 * identifies a immune bullet
	 */
	IMMUNE_BULLET;
	

}
