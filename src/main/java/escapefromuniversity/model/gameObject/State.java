package escapefromuniversity.model.gameObject;

/**
 * An enum that identifies all the possible characters state.
 */
public enum State {

    /**
     * Identifies an idle character.
     */
    IDLE,

    /**
     * Identifies a character that is facing north.
     */
    UP,

    /**
     * Identifies a character that is facing south.
     */
    DOWN,

    /**
     * Identifies a character that is facing east.
     */
    RIGHT,

    /**
     * Identifies a character that is facing west.
     */
    LEFT;

}
